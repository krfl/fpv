fn get_glyph(ch: char) -> Option<&'static [u8; 8]> {
    let upper = ch.to_ascii_uppercase();
    FONT.iter().find(|(_, c)| *c == upper).map(|(data, _)| data)
}

/// Draw a string into the framebuffer at pixel position (x, y).
/// `scale` multiplies the 8x8 font size (2 = 16x16 pixels per char).
pub fn draw_string(
    fb: &mut Framebuffer,
    text: &str,
    mut x: i32,
    y: i32,
    color: Color,
    bg: Option<Color>,
    scale: u32,
) {
    let char_w = (8 * scale) as i32;
    let char_h = (8 * scale) as i32;

    // Draw background rectangle if specified
    if let Some(bg_color) = bg {
        let pad = scale as i32;
        let total_w = text.len() as i32 * char_w + pad * 2;
        fill_rect(fb, x - pad, y - pad, total_w, char_h + pad * 2, bg_color);
    }

    for ch in text.chars() {
        if let Some(glyph) = get_glyph(ch) {
            draw_glyph(fb, glyph, x, y, color, scale);
        }
        x += char_w;
    }
}

fn draw_glyph(
    fb: &mut Framebuffer,
    glyph: &[u8; 8],
    x: i32,
    y: i32,
    color: Color,
    scale: u32,
) {
    for row in 0..8u32 {
        let bits = glyph[row as usize];
        for col in 0..8u32 {
            if bits & (0x80 >> col) != 0 {
                // Fill a scale x scale block for this pixel
                for sy in 0..scale {
                    for sx in 0..scale {
                        let px = x + (col * scale + sx) as i32;
                        let py = y + (row * scale + sy) as i32;
                        if px >= 0 && py >= 0 && (px as u32) < fb.width && (py as u32) < fb.height {
                            let idx = (py as u32 * fb.width + px as u32) as usize;
                            fb.color[idx] = color;
                        }
                    }
                }
            }
        }
    }
}

fn fill_rect(fb: &mut Framebuffer, x: i32, y: i32, w: i32, h: i32, color: Color) {
    for py in y.max(0)..(y + h).min(fb.height as i32) {
        for px in x.max(0)..(x + w).min(fb.width as i32) {
            let idx = (py as u32 * fb.width + px as u32) as usize;
            fb.color[idx] = color;
        }
    }
}
